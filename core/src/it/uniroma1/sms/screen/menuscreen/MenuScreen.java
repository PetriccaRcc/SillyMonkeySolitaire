package it.uniroma1.sms.screen.menuscreen;

import com.badlogic.gdx.Screen;

/**
 * This class allows to manage the screen classes used in the entire game, currently emtpy, but easy to use if changes are needed.
 * @author Ricciardino
 *
 */
public abstract class MenuScreen implements Screen 
{
	@Override
	public void render(float delta) {}

	@Override
	public void resize(int width, int height) {}

	@Override
	public void show() {}

	@Override
	public void hide() {}

	@Override
	public void pause() {}

	@Override
	public void resume() {}

	@Override
	public void dispose() {}
}
